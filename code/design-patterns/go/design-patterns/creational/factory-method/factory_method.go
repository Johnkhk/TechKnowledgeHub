package implementation

// uses a gun example (only has one concrete factory & method with product impl using if statement)
import "fmt"

// iGun.go declare the interface for the gun
type IGun interface {
	SetName(name string)
	SetPower(power int)
	GetName() string
	GetPower() int
}

// gun.go concrete gun struct
type Gun struct {
	name  string
	power int
}

func (g *Gun) SetName(name string) {
	g.name = name
}

func (g *Gun) GetName() string {
	return g.name
}

func (g *Gun) SetPower(power int) {
	g.power = power
}

func (g *Gun) GetPower() int {
	return g.power
}

// ak47.go declares the struct for the AK47 gun
type Ak47 struct {
	Gun
}

func newAk47() IGun {
	return &Ak47{
		Gun: Gun{
			name:  "AK47 gun",
			power: 4,
		},
	}
}

// musket.go declares the struct for the musket gun
type musket struct {
	Gun
}

func newMusket() IGun {
	return &musket{
		Gun: Gun{
			name:  "Musket gun",
			power: 1,
		},
	}
}

// gunFactory.go declares the factory method to get the gun
func GetGun(gunType string) (IGun, error) {
	if gunType == "ak47" {
		return newAk47(), nil
	}
	if gunType == "musket" {
		return newMusket(), nil
	}
	return nil, fmt.Errorf("Wrong gun type passed")
}
